package pessoaEndereco;

public class Endereco {
	
	//Atributos
	String rua;
	int numero;
	String complemento;
	String bairro;
	
	//Construtor
	public Endereco(String rua, int numero, String complemento, String bairro) {
		this.rua = rua;
		this.numero = numero;
		this.complemento = complemento;
		this.bairro = bairro;
	}

}
